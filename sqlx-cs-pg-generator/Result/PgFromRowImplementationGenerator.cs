using System.Collections.Immutable;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace Sqlx.Postgres.Generator.Result;

internal class PgFromRowImplementationGenerator : ISourceGenerationPipeline<PgFromRowToGenerate>
{
    public string AttributeName => "Sqlx.Postgres.Generator.Result.FromRowAttribute";

    public bool IsValidSyntax(SyntaxNode node, CancellationToken cancellationToken) =>
        node.IsProductType;

    public PgFromRowToGenerate? CreateGenerationContext(
        GeneratorAttributeSyntaxContext context,
        CancellationToken cancellationToken)
    {
        if (context.TargetSymbol is not INamedTypeSymbol typeSymbol ||
            context.TargetNode is not TypeDeclarationSyntax typeDeclarationSyntax)
        {
            return null;
        }

        cancellationToken.ThrowIfCancellationRequested();
        return new PgFromRowToGenerate(typeSymbol, typeDeclarationSyntax);
    }

    public void ExecuteGeneration(
        SourceProductionContext context,
        ImmutableArray<PgFromRowToGenerate> implementationsToGenerate)
    {
        ExecutePgFromRowGeneration(context, implementationsToGenerate);
    }

    public static void ExecutePgFromRowGeneration(
        in SourceProductionContext context,
        ImmutableArray<PgFromRowToGenerate> implementationsToGenerate)
    {
        if (implementationsToGenerate.IsEmpty)
        {
            return;
        }

        foreach (PgFromRowToGenerate pgFromRowToGenerate in implementationsToGenerate)
        {
            context.CancellationToken.ThrowIfCancellationRequested();

            if (!pgFromRowToGenerate.Validate(context))
            {
                continue;
            }

            var fromRowImplementation = GeneratePgFromRowTypeImplementation(pgFromRowToGenerate);
            context.AddSource(
                $"{pgFromRowToGenerate.ShortName}.IFromRow.g.cs",
                SourceText.From(fromRowImplementation, Encoding.UTF8));
        }
    }

    private static string GeneratePgFromRowTypeImplementation(
        PgFromRowToGenerate pgFromRowToGenerate)
    {
        StringBuilder builder = new(
            """
            // <auto-generated/>
            #nullable enable

            using System.Buffers;
            using Sqlx.Core.Result;
            using Sqlx.Postgres.Result;
            using Sqlx.Postgres.Type;
            """);
        builder.AppendLine();
        if (!string.IsNullOrEmpty(pgFromRowToGenerate.ContainingNamespace))
        {
            builder.AppendLine();
            builder.Append("namespace ")
                .Append(pgFromRowToGenerate.ContainingNamespace)
                .AppendLine(";");
        }

        builder.AppendLine();
        builder.Append("public partial ")
            .Append(pgFromRowToGenerate.IsStruct ? "struct " : "class ")
            .Append(pgFromRowToGenerate.ShortName)
            .Append(" : IFromRow<IPgDataRow, ")
            .Append(pgFromRowToGenerate.ShortName)
            .AppendLine(">");
        builder.AppendLine("{");
        builder.Append("    public static ")
            .Append(pgFromRowToGenerate.ShortName)
            .AppendLine(" FromRow(IPgDataRow dataRow)");
        builder.AppendLine("    {");
        builder.Append("        return new ")
            .Append(pgFromRowToGenerate.ShortName);
        if (!pgFromRowToGenerate.ConstructorParameters.IsEmpty)
        {
            builder.AppendLine("(");
            AppendRowFields(
                builder,
                pgFromRowToGenerate.ConstructorParameters,
                assignmentChars: ": ",
                allowTrailingCommas: false);
            builder.Append(')');
        }
        else
        {
            builder.AppendLine();
        }

        if (!pgFromRowToGenerate.InitProperties.IsEmpty)
        {
            builder.AppendLine("        {");
            AppendRowFields(
                builder,
                pgFromRowToGenerate.InitProperties,
                assignmentChars: " = ",
                allowTrailingCommas: true);
            builder.AppendLine("        };");
        }
        else
        {
            builder.AppendLine(";");
        }

        builder.AppendLine("    }");
        builder.AppendLine("}");
        return builder.ToString();
    }

    private static void AppendRowFields(
        StringBuilder builder,
        in ImmutableArray<RowField> rowFields,
        string assignmentChars,
        bool allowTrailingCommas)
    {
        for (var index = 0; index < rowFields.Length; index++)
        {
            RowField rowField = rowFields[index];
            builder.Append("            ")
                .Append(rowField.Name)
                .Append(assignmentChars);

            switch (rowField)
            {
                case { Flatten: true }:
                    builder.AppendFullName(rowField.FieldType)
                        .Append(".FromRow(dataRow)");
                    break;
                case { IsJson: true, FieldType.IsNullable: true }:
                    builder.Append("dataRow.GetJson<")
                        .AppendFullName(rowField.FieldType.AsNotNullType())
                        .Append(">(\"")
                        .Append(rowField.FieldName)
                        .Append("\")");
                    break;
                case { IsJson: true }:
                    builder.Append("dataRow.GetJsonNotNull<")
                        .AppendFullName(rowField.FieldType)
                        .Append(">(\"")
                        .Append(rowField.FieldName)
                        .Append("\")");
                    break;
                case { FieldType: { IsDbType: true, IsNullable: true, IsValueType: true } }:
                    builder.Append("dataRow.GetPgVal<")
                        .AppendFullName(rowField.FieldType)
                        .Append(">(\"")
                        .Append(rowField.FieldName)
                        .Append("\")");
                    break;
                case { FieldType: { IsDbType: true, IsNullable: true, IsValueType: false } }:
                    builder.Append("dataRow.GetPgRef<")
                        .AppendFullName(rowField.FieldType)
                        .Append(">(\"")
                        .Append(rowField.FieldName)
                        .Append("\")");
                    break;
                case { FieldType: { IsDbType: true, IsNullable: false } }:
                    builder.Append("dataRow.GetPgNotNull<")
                        .AppendFullName(rowField.FieldType)
                        .Append(">(\"")
                        .Append(rowField.FieldName)
                        .Append("\")");
                    break;
                case { FieldType: INamedTypeSymbol { IsPgEnum: true, IsNullable: true } }:
                    builder.Append("dataRow.GetPgVal<")
                        .AppendFullName(rowField.FieldType)
                        .Append(',')
                        .Append(rowField.FieldType.GetIPgDbType())
                        .Append(">(\"")
                        .Append(rowField.FieldName)
                        .Append("\")");
                    break;
                case { FieldType: INamedTypeSymbol { IsPgEnum: true } }:
                    builder.Append("dataRow.GetPgNotNull<")
                        .AppendFullName(rowField.FieldType)
                        .Append(',')
                        .Append(rowField.FieldType.GetIPgDbType())
                        .Append(">(\"")
                        .Append(rowField.FieldName)
                        .Append("\")");
                    break;
                case { FieldType: INamedTypeSymbol { IsDecodableEnum: true, IsNullable: true } }:
                    builder.Append("dataRow.Get")
                        .Append(rowField.FieldType.AsNotNullType())
                        .Append("(\"")
                        .Append(rowField.FieldName)
                        .Append("\")");
                    break;
                case { FieldType: INamedTypeSymbol { IsDecodableEnum: true, IsNullable: false } }:
                    builder.Append("dataRow.Get")
                        .Append(rowField.FieldType.Name)
                        .Append("NotNull").Append("(\"")
                        .Append(rowField.FieldName)
                        .Append("\")");
                    break;
                case { FieldType: { IsNullable: true, IsValueType: true } }:
                    builder.Append("dataRow.GetPgVal<")
                        .AppendFullName(rowField.FieldType.AsNotNullType())
                        .Append(',')
                        .Append(rowField.FieldType.GetIPgDbType())
                        .Append(">(\"")
                        .Append(rowField.FieldName)
                        .Append("\")");
                    break;
                case { FieldType: { IsNullable: true, IsValueType: false } }:
                    builder.Append("dataRow.GetPgRef<")
                        .AppendFullName(rowField.FieldType)
                        .Append(',')
                        .Append(rowField.FieldType.GetIPgDbType())
                        .Append(">(\"")
                        .Append(rowField.FieldName)
                        .Append("\")");
                    break;
                default:
                    builder.Append("dataRow.GetPgNotNull<")
                        .AppendFullName(rowField.FieldType)
                        .Append(',')
                        .Append(rowField.FieldType.GetIPgDbType())
                        .Append(">(\"")
                        .Append(rowField.FieldName)
                        .Append("\")");
                    break;
            }

            if (allowTrailingCommas || index != rowFields.Length - 1)
            {
                builder.AppendLine(",");
            }
        }
    }
}
