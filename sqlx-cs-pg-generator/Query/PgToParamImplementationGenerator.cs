using System.Collections.Immutable;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace Sqlx.Postgres.Generator.Query;

internal static class PgToParamImplementationGenerator
{
    public static PgToParamToGenerate? GetPgToParamToGenerate(
        GeneratorAttributeSyntaxContext context,
        CancellationToken cancellationToken)
    {
        if (context.TargetSymbol is not INamedTypeSymbol typeSymbol ||
            context.TargetNode is not TypeDeclarationSyntax typeDeclarationSyntax)
        {
            return null;
        }

        cancellationToken.ThrowIfCancellationRequested();
        return new PgToParamToGenerate(typeSymbol, typeDeclarationSyntax);
    }

    public static void ExecutePgToParamGeneration(
        SourceProductionContext context,
        ImmutableArray<PgToParamToGenerate?> implementationsToGenerate)
    {
        if (implementationsToGenerate.IsEmpty)
        {
            return;
        }

        foreach (var toGenerate in implementationsToGenerate)
        {
            context.CancellationToken.ThrowIfCancellationRequested();
            if (toGenerate is not { } pgToParamToGenerate)
            {
                continue;
            }

            if (!pgToParamToGenerate.Validate(context))
            {
                continue;
            }

            var fromRowImplementation = GeneratePgBindManyTypeImplementation(pgToParamToGenerate);
            context.AddSource(
                $"{pgToParamToGenerate.ShortName}.IBindMany.g.cs",
                SourceText.From(fromRowImplementation, Encoding.UTF8));
        }
    }

    private static string GeneratePgBindManyTypeImplementation(
        PgToParamToGenerate pgToParamToGenerate)
    {
        StringBuilder builder = new(
            """
            // <auto-generated/>
            #nullable enable

            using System.Buffers;
            using Sqlx.Core.Query;
            using Sqlx.Postgres.Query;
            """);
        builder.AppendLine();
        if (!string.IsNullOrEmpty(pgToParamToGenerate.ContainingNamespace))
        {
            builder.AppendLine();
            builder.Append("namespace ")
                .Append(pgToParamToGenerate.ContainingNamespace)
                .AppendLine(";");
        }
        builder.AppendLine();
        builder.Append("public partial ")
            .Append(pgToParamToGenerate.IsStruct ? "struct " : "class ")
            .Append(pgToParamToGenerate.ShortName)
            .AppendLine(" : IBindMany<IPgBindable>");
        builder.AppendLine("{");
        builder.AppendLine("    public void BindMany(IPgBindable bindable)");
        builder.AppendLine("    {");
        foreach (var property in pgToParamToGenerate.Properties)
        {
            builder.Append("        bindable.Bind(this.")
                .Append(property)
                .AppendLine(");");  
        }
        builder.AppendLine("    }");
        builder.AppendLine("}");
        return builder.ToString();
    }
}
